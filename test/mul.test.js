'use strict';
const mul = require('../lib/mul');

test('23456789123456789123456789 * (-1) to equal -23456789123456789123456789', () =>{
  expect(mul('23456789123456789123456789','-1')).toBe('-23456789123456789123456789');
});

test('23456789123456789123456789 * 0 to equal 0', () =>{
  expect(mul('23456789123456789123456789','0')).toBe('0');
});

test('23456789123456789123456789 * 33332 to equal 781861695063061695063061690948', () =>{
  expect(mul('23456789123456789123456789','33332')).toBe('781861695063061695063061690948');
});

test('-23456789123456789123456789 * - 1 to equal 23456789123456789123456788', () =>{
  expect(mul('-23456789123456789123456789','-1')).toBe('23456789123456789123456789');
});

test('-2 * (-2) to equal 4', () =>{
  expect(mul("-2", "-2")).toBe("4");
});

test('2 * (-2) to equal -4', () =>{
  expect(mul("2", "-2")).toBe("-4");
});

test('-5 * 4 to equal 20', () =>{
  expect(mul("-5", "4")).toBe("-20");
});

test('-5 * (-6) to equal 30', () =>{
  expect(mul("-5", "-6")).toBe("30");
});